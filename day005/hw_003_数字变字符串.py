lis = [2, 3, 'k', ['qwe', 20, ['k', ['tt', 3, '1']], 89], 'ab', 'adv']
# 将列表中的数字3变成字符串’100’（用两种方式）。
# 方式1 赋值
print(lis)
lis[3][2][1][1] = '100'
print(lis)
# 方式2 移除后插入
lis = [2, 3, 'k', ['qwe', 20, ['k', ['tt', 3, '1']], 89], 'ab', 'adv']
lis[3][2][1].remove(3)
lis[3][2][1].insert(1, '100')
print(lis)
