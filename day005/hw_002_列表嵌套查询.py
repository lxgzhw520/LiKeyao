# 方式1 赋值
print(lis)
lis[3][2][1][0] = "TT"
print(lis)
# 方式2 修改
lis[3][2][1][0] = lis[3][2][1][0].lower()
print(lis)
lis[3][2][1][0] = lis[3][2][1][0].upper()
print(lis)
